public class PrimitiveTypes{
	public static void main(String args[]){
	//primitive types
	int i=10;
	short s=5;
	byte b=100;
	long l=300;
	float f=1078786.760f;
	double d=400.0;
	char ch='C';
	boolean bool=true;//false
	System.out.printf("%-50s\t%d\t%s","Primitive Data Types in JAVA:",8,"Types");
	System.out.println("\nValue in int:"+i);
	System.out.printf("Value in int:03%d",i);
	System.out.printf("\nValue in short:%d",s);
	System.out.printf("\nValue in byte:%d",b);
	System.out.printf("\nValue in long:%d",l);
	System.out.printf("\nValue in float:%,f",f);
	System.out.printf("\nValue in double:%.2f",d);
	System.out.printf("\nValue in boolean:%b",bool);
	System.out.printf("\nValue in character:%c",ch);
	
	
}
}